// 一维数组转二维数组
const arrOneChangeTwo = function(arr, num) {
	if(!Array.isArray(arr)) {
		return [];
	}
	if(Array.isArray(arr) && arr.length <= 1) {
		return arr;
	}
	let a = [];
	while(arr.length > 0) {
		a.push(arr.splice(0, num))
	}
	return a;
}